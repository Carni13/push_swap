#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*nextlst;

	if (!(*alst))
		*alst = new;
	else
	{
		nextlst = (*alst);
		while (nextlst->next)
			nextlst = nextlst->next;
		nextlst->next = new;
	}
}
